#include <stdio.h> // รวมไลบรารีมาตรฐานสำหรับการใช้งานฟังก์ชันการแสดงผล

int main() { // ฟังก์ชันหลักของโปรแกรม
    for (int i = 2; i <= 10; i = i + 2) { 
    // for loop จะแบ่งการทำงานเป็น 3 ส่วน:
    // 1. int i = 2;  : กำหนดค่าเริ่มต้นของตัวแปร 'i'
    // 2. i <= 10;    : เงื่อนไขการทำงานของลูป (ลูปจะทำงานต่อเมื่อ 'i' มีค่าไม่เกิน 10)
    // 3. i = i + 2   : การเพิ่มค่า 'i' ทีละ 2 ในทุกๆ รอบที่ทำงาน
        printf("%d ", i); // แสดงค่าของ 'i' ซึ่งเป็นเลขคู่
    }
    printf("\nEnd of loop\n"); // เมื่อลูปทำงานเสร็จสิ้น ให้แสดงข้อความนี้

    return 0; // ส่งค่ากลับ 0 เพื่อบอกว่าการทำงานของโปรแกรมเสร็จสมบูรณ์
} 