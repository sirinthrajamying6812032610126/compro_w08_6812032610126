#include <stdio.h> // รวมไลบรารีมาตรฐานสำหรับการใช้งานฟังก์ชันการแสดงผล

int main() { // ฟังก์ชันหลักของโปรแกรม
    int i = 2; // กำหนดค่าเริ่มต้นของตัวแปร 'i' ให้เท่ากับ 2 เนื่องจากโจทย์ต้องการให้เริ่มจากเลขคู่ 2

    while (i <= 10) {  // while loop จะทำงานตราบใดที่ค่าของ 'i' น้อยกว่าหรือเท่ากับ 10
        printf("%d ", i);  // แสดงค่าของ 'i' (ซึ่งเป็นเลขคู่) ออกทางหน้าจอ

        i = i + 2; // เพิ่มค่า 'i' ขึ้น 2 เพื่อให้ได้เลขคู่ตัวถัดไป (เช่น จาก 2 เป็น 4, จาก 4 เป็น 6)
    }
    printf("\nEnd of loop\n"); // เมื่อลูปทำงานเสร็จสิ้น (ค่า 'i' เกิน 10) ให้แสดงข้อความนี้

    return 0; // ส่งค่ากลับ 0 เพื่อบอกว่าการทำงานของโปรแกรมเสร็จสมบูรณ์
}